package com.example.user.taskapp;

import android.content.Context;
import android.content.Intent;
import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentPagerAdapter;
import android.support.v4.app.FragmentManager;

public class MyAdapter extends FragmentPagerAdapter {

    private Context myContext;
    int totalTabs;

    public MyAdapter(Context context, FragmentManager fm, int totalTabs) {
        super(fm);
        myContext = context;
        this.totalTabs = totalTabs;
    }

    // this is for fragment tabs
    @Override
    public Fragment getItem(int position) {
        switch (position) {
            case 0:
                Combo_Fragment comboFragment = new Combo_Fragment();
                 return comboFragment;

            case 1:
                Drinks_Fragment drinksFragment = new Drinks_Fragment();
                return drinksFragment;
            case 2:
                Snacks_Fragment snacksFragment = new Snacks_Fragment();
                return snacksFragment;
            default:
                return null;
        }
    }
    // this counts total number of tabs
    @Override
    public int getCount() {
        return totalTabs;
    }
}